package com.qiuzhi30Switch;

public class Test_A {
    public static void main(String[] args) {
      //  switch
        int i = 1;
        switch(i) {
            case 1:
                System.out.println("Monday");
                break;
            case 2:
                System.out.println("Tuesday");
                break;
            default:
                System.out.println("No");
                break;
        }
    }
}
