package com.qiuzhi32While;

public class Test_A {
    public static void main(String[] args) {
        int i = 1;
        while(i <= 5) {
            System.out.println(i);
            i ++;
        }

        int m = 1;
        do {
            System.out.println(m);
            m ++;
        } while (m <= 5);
    }
}
